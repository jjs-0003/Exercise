package jp.co.jjs.java_seminar.self_study.q5_3;

public class Sphere {

    private double radius;
    private double center;

    public Sphere(double radius, double center) {
        this.radius = radius;
        this.center = center;
    }

    /**
     * @return radius
     */
    public double getRadius() {
        return radius;
    }

    /**
     * @return center
     */
    public double getCenter() {
        return center;
    }

}
